/** 今日の問題インデックス（日付ベース・全画面共通） */
export function getDailyFermiIndex(): number {
  return Math.floor(Date.now() / 86400000) % FERMI_POOL.length
}

/** 今日の問題を返す */
export function getDailyFermi(): FermiQuestion {
  return FERMI_POOL[getDailyFermiIndex()]
}
