// drillData.ts — フェルミ推定ドリル定義
// コースが「複数レッスンを通じた体系学習」なのに対し、
// ドリルは「単一の問いに対して、分解 → 仮置き → 計算 → 検証 を1本ずつ通す」演習。

export type DrillGroupId =
  | 'market-size'      // 市場規模推定
  | 'store-revenue'    // 店舗の収益予測

export type DrillLevel = '初級' | '中級' | '上級'

export type DrillAssumption = {
  label: string
  value: string
  note?: string
}

export type Drill = {
  id: string
  title: string
  group: DrillGroupId
  level: DrillLevel
  estimatedMinutes: number
  prompt: string                 // 問い
  setup?: string                 // 前提・状況設定（任意）
  hint: string                   // 着眼点
  approach: string[]             // 分解アプローチ（ステップ）
  assumptions: DrillAssumption[] // 置きの数字
  formula: string                // 数式（テキスト）
  calculation: string[]          // 計算ステップ
  answer: string                 // 推定値（レンジ込み）
  sanityCheck?: string           // 妥当性チェック（任意）
  takeaways: string[]            // 振り返りポイント
}

export type DrillGroup = {
  id: DrillGroupId
  label: string
  description: string
  image?: string
}

