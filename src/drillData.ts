// ヘルパー
export function getDrillsByGroup(group: DrillGroupId): Drill[] {
  return DRILLS.filter(d => d.group === group)
}

export function getDrillById(id: string): Drill | undefined {
  return DRILLS.find(d => d.id === id)
}
